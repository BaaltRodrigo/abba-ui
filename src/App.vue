<template>
  <div id="app">
    <router-view/>
  </div>
</template>

<script>
import { mapActions } from 'vuex'
export default {
  name: 'app',

  methods: {
    ...mapActions('auth', ['getCurrentUser']),
  },

  async created() {
    // No jwt cookie
    if (!this.$cookies.get('jwt')) this.$router.push({name: 'Login'})
  }
}
</script>

